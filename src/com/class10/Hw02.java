package com.class10;

public class Hw02 {

	public static void main(String[] args) {

		String[] animal= {"elephant", "Lion", "Tiger", "Bear", "Horse", "Monkey"};
		
		for(String a:animal) {
			System.out.print(a+" ");
		}
		
		System.out.println();
		
		for(int b=0; b<animal.length; b++) {
			System.out.print(animal[b]+" ");
		}
		
		
	}

}
