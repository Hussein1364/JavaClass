package com.class07;

public class LoopStatement {

	public static void main(String[] args) {

		int time=13;
		
		while(time<12) {
			System.out.println("hello");
			time++;
		}
		
		///Print 1  to   10
		int b=20;
		while (b<=40) {
			System.out.print(b+",");
			b++;
			
		}
		System.out.println("==============");
		int c=10;
		while (c>=1) {
			System.out.print(c+",");
			c--;
			
		}
		
	}

}
