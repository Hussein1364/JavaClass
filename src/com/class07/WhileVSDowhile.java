package com.class07;

public class WhileVSDowhile {

	public static void main(String[] args) {

		
		int num=8;
		while(num<=5) {
			System.out.print(num);
			num++;
		}
		
		System.out.println("*******Do while");
		
		int num1=8;
		do {
			System.out.print(num1);
			num1++;
		}while (num1<=5);
		
		
		
		
	}

}
